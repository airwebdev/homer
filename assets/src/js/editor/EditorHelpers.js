    homer.vars = {};

    homer.animationsNames = [
        __('Wipe', homer.name),
        __('Typewriter', homer.name),
        __('Peek In', homer.name),
        __('Flip', homer.name),
        __('Bounce', homer.name),
        __('Swivel', homer.name),
        __('Rise', homer.name),
        __('Rotate', homer.name),
        __('Drop', homer.name),
        __('Fly In', homer.name),
        __('Slow Wipe', homer.name),
        __('Zoom', homer.name),
        __('Appear', homer.name),
        __('Grow', homer.name),
        __('Stretch', homer.name),
        __('Wave', homer.name),
        __('Spinner', homer.name),
        __('LetterFall', homer.name),
    ];

    homer.animationAvailableOptionsNames = homer.animationsNames.map(
        animationName => homer.name + '_' + animationName + '_available',
    );

    homer.animationIsFavouriteIconsNamesOptions = homer.animationsNames.map(
        animationName => homer.name + '_' + animationName + '_favourite_icon',
    );

    // select text of element
    homer.helpers.selectElement = el => {
        if (
            window.getSelection().focusNode &&
            window.getSelection().focusNode.classList &&
            !window.getSelection().focusNode.classList.contains('homer-typewriter')
        ) {
            let sel, range;
            if (window.getSelection && document.createRange) {
                //Browser compatibility
                sel = window.getSelection();
                if (sel.toString() == '') {
                    //no text selection
                    window.setTimeout(function () {
                        range = document.createRange(); //range object
                        range.selectNodeContents(el); //sets Range
                        sel.removeAllRanges(); //remove all ranges from selection
                        sel.addRange(range); //add Range to a Selection.
                    }, 1);
                }
            } else if (document.selection) {
                //older ie
                sel = document.selection.createRange();
                if (sel.text == '') {
                    //no text selection
                    range = document.body.createTextRange(); //Creates TextRange object
                    range.moveToElementText(el); //sets Range
                    range.select(); //make selection.
                }
            }
        }
    };

    homer.helpers.startAnimationEvent = el => {
        const startAnimation = new Event('TypewriterStart', { bubbles: true });
        el && el.dispatchEvent(startAnimation);
    };

    homer.helpers.restartAnimationEvent = el => {
        const startAnimation = new Event('TypewriterRestart', { bubbles: true });
        el && el.dispatchEvent(startAnimation);
    };

    homer.helpers.stopAnimationEvent = el => {
        const stopAnimation = new Event('TypewriterStop', { bubbles: true });
        el.dispatchEvent(stopAnimation);
    };

    homer.helpers.startStopAnimationEvent = el => {
        homer.helpers.startAnimationEvent(el);
        // start animation event listner
        el &&
            el.addEventListener('click', function () {
                homer.helpers.stopAnimationEvent(el);
            });
    };

    homer.helpers.ready = callback => {
        wp.domReady(() => {
            if (wp.components) {
                callback();
            }
        });
    };

    homer.helpers.makeid = () => {
        const result = [];
        const characters = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789';
        const charactersLength = characters.length;
        for (let i = 0; i < 7; i++) {
            result.push(characters.charAt(Math.floor(Math.random() * charactersLength)));
        }
        return result.join('');
    };
